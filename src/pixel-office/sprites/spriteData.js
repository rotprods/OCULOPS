// ── Sprite generation ───────────────────────────────────────

const _spriteCache = new Map()

export function getCharacterSprites(paletteIndex, hueShift = 0) {
    const key = `${paletteIndex}:${hueShift}`
    const cached = _spriteCache.get(key)
    if (cached) return cached

    const pal = CHARACTER_PALETTES[paletteIndex % CHARACTER_PALETTES.length]
    const r = (t) => resolveTemplate(t, pal)
    const rf = (t) => resolveTemplate(flipHorizontal(t), pal)

    // Simplified: use standing frame for all walk frames where templates aren't defined
    const sprites = {
        walk: {
            [Direction.DOWN]: [r(CHAR_WALK_DOWN_1), r(CHAR_WALK_DOWN_2), r(CHAR_WALK_DOWN_3), r(CHAR_WALK_DOWN_2)],
            [Direction.UP]: [r(CHAR_WALK_UP_2), r(CHAR_WALK_UP_2), r(CHAR_WALK_UP_2), r(CHAR_WALK_UP_2)],
            [Direction.RIGHT]: [r(CHAR_WALK_RIGHT_2), r(CHAR_WALK_RIGHT_2), r(CHAR_WALK_RIGHT_2), r(CHAR_WALK_RIGHT_2)],
            [Direction.LEFT]: [rf(CHAR_WALK_RIGHT_2), rf(CHAR_WALK_RIGHT_2), rf(CHAR_WALK_RIGHT_2), rf(CHAR_WALK_RIGHT_2)],
        },
        typing: {
            [Direction.DOWN]: [r(CHAR_DOWN_TYPE_1), r(CHAR_DOWN_TYPE_2)],
            [Direction.UP]: [r(CHAR_UP_TYPE_1), r(CHAR_UP_TYPE_2)],
            [Direction.RIGHT]: [r(CHAR_RIGHT_TYPE_1), r(CHAR_RIGHT_TYPE_2)],
            [Direction.LEFT]: [rf(CHAR_RIGHT_TYPE_1), rf(CHAR_RIGHT_TYPE_2)],
        },
        reading: {
            [Direction.DOWN]: [r(CHAR_DOWN_TYPE_1), r(CHAR_DOWN_TYPE_2)],
            [Direction.UP]: [r(CHAR_UP_TYPE_1), r(CHAR_UP_TYPE_2)],
            [Direction.RIGHT]: [r(CHAR_RIGHT_TYPE_1), r(CHAR_RIGHT_TYPE_2)],
            [Direction.LEFT]: [rf(CHAR_RIGHT_TYPE_1), rf(CHAR_RIGHT_TYPE_2)],
        },
    }

    _spriteCache.set(key, sprites)
    return sprites
}
